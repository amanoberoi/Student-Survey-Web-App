//This file contains the getters and setters for the mean and standard deviation.
package student;

public class WinningResult {

	private double mean;
	private double stddev;
	
	public double getMean() {
		return mean;
	}
	public void setMean(double mean) {
		this.mean = mean;
	}
	public double getStddev() {
		return stddev;
	}
	public void setStddev(double stddev) {
		this.stddev = stddev;
	}
	
	
}
